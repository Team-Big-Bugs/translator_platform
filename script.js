// Current language state
let currentLang = localStorage.getItem('language') || 'zh-TW';

// Initialize on page load
document.addEventListener('DOMContentLoaded', function () {
  // Set initial language
  setLanguage(currentLang);

  // Add click handlers to language switcher
  const langOptions = document.querySelectorAll('.lang-option');
  langOptions.forEach(option => {
    option.addEventListener('click', function () {
      const lang = this.textContent.includes('繁中') ? 'zh-TW' : 'ko-KR';
      setLanguage(lang);
    });
  });

  // FAQ Accordion
  const faqQuestions = document.querySelectorAll('.faq-question');
  faqQuestions.forEach(question => {
    question.addEventListener('click', () => {
      const item = question.parentElement;
      const isActive = item.classList.contains('active');

      // Close all other items
      document.querySelectorAll('.faq-item').forEach(otherItem => {
        otherItem.classList.remove('active');
      });

      // Toggle current item
      if (!isActive) {
        item.classList.add('active');
      }
    });
  });
  // Mouse Spotlight Effect for Stats Grid
  const statsGrid = document.querySelector('.stats-grid');
  if (statsGrid) {
    statsGrid.addEventListener('mousemove', (e) => {
      const rect = statsGrid.getBoundingClientRect();
      const x = e.clientX - rect.left;
      const y = e.clientY - rect.top;
      statsGrid.style.setProperty('--mouse-x', `${x}px`);
      statsGrid.style.setProperty('--mouse-y', `${y}px`);
    });
  }

  // Number Count-Up Animation
  const observerOptions = {
    threshold: 0.5
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const el = entry.target;
        const targetText = el.innerText;
        const targetValue = parseInt(targetText.replace(/\D/g, ''));

        if (!isNaN(targetValue)) {
          let startTimestamp = null;
          const duration = 2000;

          const step = (timestamp) => {
            if (!startTimestamp) startTimestamp = timestamp;
            const progress = Math.min((timestamp - startTimestamp) / duration, 1);
            const currentValue = Math.floor(progress * targetValue);

            // Keep the suffix (e.g. "+")
            const suffix = targetText.replace(/[0-9]/g, '');
            el.innerText = currentValue + suffix;

            if (progress < 1) {
              window.requestAnimationFrame(step);
            } else {
              el.innerText = targetText; // Ensure exact final value
            }
          };

          window.requestAnimationFrame(step);
          observer.unobserve(el);
        }
      }
    });
  }, observerOptions);

  document.querySelectorAll('.stat-number').forEach(el => observer.observe(el));
});

// Set language function
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('language', lang);

  // Add transition class
  document.body.classList.add('lang-transitioning');

  // Update all translatable elements
  setTimeout(() => {
    // 1. Update text content from translations
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const key = element.getAttribute('data-i18n');
      if (translations[lang] && translations[lang][key]) {
        element.innerHTML = translations[lang][key];
      }
    });

    // 2. Re-trigger animations for visible numbers if switching language might reset them? 
    // Actually, simple text replacement is fine. The observer handles initial load.

    // Update active state on language switcher
    document.querySelectorAll('.lang-option').forEach(option => {
      if (lang === 'zh-TW') {
        if (option.textContent.includes('繁中')) option.classList.add('active');
        else option.classList.remove('active');
      } else {
        if (option.textContent.includes('한국어')) option.classList.add('active');
        else option.classList.remove('active');
      }
    });

    // Remove transition class
    document.body.classList.remove('lang-transitioning');
  }, 150);
}
